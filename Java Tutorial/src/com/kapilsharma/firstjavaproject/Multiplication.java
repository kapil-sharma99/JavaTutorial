package com.kapilsharma.firstjavaproject;

public class Multiplication {
	void print() {
		for(int i = 1; i <= 10; i++) {
			System.out.printf("%d * %d = %d", 5, i, 5*i).println();
		}
	}
	
	void print(int n) {
		for(int i = 1; i <= 10; i++) {
			System.out.printf("%d * %d = %d", n, i, n*i).println();
		}
	}
}
